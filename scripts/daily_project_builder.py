def sanitize_text(text: str) -> str:
    replacements = {
        "Codex": "the project",
        "codex": "the project",
        "AI Contribution": "Project Notes",
        "ai contribution": "project notes",
        "automation": "workflow",
        "Automation": "Workflow",
    }
    for old, new in replacements.items():
        text = text.replace(old, new)
    return text


def sanitize_project(project: dict) -> dict:
    project = json.loads(json.dumps(project))
    project["title"] = sanitize_text(str(project.get("title", FALLBACK_PROJECT["title"]))).strip()
    project["track"] = sanitize_text(str(project.get("track", "Bioinformatics"))).strip()
    if project["track"] not in TRACKS:
        project["track"] = "Bioinformatics"
    project["concept"] = sanitize_text(str(project.get("concept", "Beginner biotechnology learning"))).strip()
    project["slug"] = slugify(str(project.get("slug") or project["title"]))
    project["lesson_email"] = sanitize_text(str(project.get("lesson_email", "")))

    files = []
    for file in project.get("files", []):
        path = str(file.get("path", "")).strip().replace("\\", "/")
        if not path or path.startswith("/") or ".." in Path(path).parts:
            continue
        files.append({"path": path, "content": sanitize_text(str(file.get("content", "")))})
    project["files"] = files

    lower_blob = json.dumps(project).lower()
    for phrase in FORBIDDEN_PHRASES:
        if phrase in lower_blob:
            raise ValueError(f"Forbidden phrase found: {phrase}")
    if not any(file["path"].lower() == "readme.md" for file in files):
        raise ValueError("Project is missing README.md")
    if not any(file["path"].endswith(".py") for file in files):
        raise ValueError("Project is missing a Python file")
    return project


def choose_project(date_text: str) -> dict:
    try:
        proposals = [extract_json(openai_text(proposal_prompt(i, date_text), 1200)) for i in range(1, 4)]
        judge = extract_json(openai_text(judge_prompt(proposals, date_text), 1000))
        winner_index = min(max(int(judge.get("winner_index", 0)), 0), 2)
        built = extract_json(openai_text(build_prompt(proposals[winner_index], judge, date_text), 7000))
        return sanitize_project(built)
    except Exception as exc:
        print(f"Model-guided selection failed, using fallback project: {exc}")
        return sanitize_project(FALLBACK_PROJECT)


def unique_folder_name(date_text: str, slug: str) -> str:
    base = f"{date_text}-{slug}"
    candidate = base
    counter = 2
    while (PROJECTS_DIR / candidate).exists():
        candidate = f"{base}-{counter}"
        counter += 1
    return candidate


def update_root_readme(project: dict, folder_name: str, date_text: str) -> None:
    readme = README_PATH.read_text(encoding="utf-8") if README_PATH.exists() else "# Daily Biotech Projects\n"
    project_row = f"| {date_text} | {project['title']} | {project['concept']} | [projects/{folder_name}](projects/{folder_name}) |"
    chrono_row = f"| {date_text} | {project['track']} | [{project['title']}](projects/{folder_name}) |"
    heading = f"### {project['track']}"

    if heading in readme and project_row not in readme:
        heading_index = readme.find(heading)
        next_heading = readme.find("\n### ", heading_index + len(heading))
        if next_heading == -1:
            next_heading = readme.find("\n## Chronological Index", heading_index)
        if next_heading != -1:
            readme = readme[:next_heading].rstrip() + f"\n{project_row}\n" + readme[next_heading:]

    if chrono_row not in readme and "\n## Portfolio Focus" in readme:
        marker = readme.find("\n## Portfolio Focus")
        readme = readme[:marker].rstrip() + f"\n{chrono_row}\n" + readme[marker:]
    README_PATH.write_text(readme, encoding="utf-8")


def line_explanation(code: str) -> str:
    parts = ["Line-by-Line Code Explanation:", ""]
    for number, text in enumerate(code.splitlines(), start=1):
        stripped = text.strip()
        if not stripped:
            meaning = "This blank line separates sections so the script is easier to read."
        elif stripped.startswith('"""'):
            meaning = "This describes the purpose of the script."
        elif stripped.startswith("from __future__"):
            meaning = "This keeps modern Python type-hint behavior consistent."
        elif stripped.startswith("import ") or stripped.startswith("from "):
            meaning = "This imports a module the script needs."
        elif stripped.startswith("def "):
            meaning = "This starts a reusable function."
        elif stripped.startswith("return "):
            meaning = "This sends a result back from the function."
        elif stripped.startswith("if __name__"):
            meaning = "This runs main() only when the file is opened directly."
        elif "argparse" in stripped or "add_argument" in stripped:
            meaning = "This helps the script accept input from the command line."
        elif "print(" in stripped:
            meaning = "This displays the result for the user."
        else:
            meaning = "This line supports the main project logic."
        parts.append(f"Line {number}: `{text}`")
        parts.append(f"Explanation: {meaning}")
        parts.append("")
    return "\n".join(parts)


def main_code_files(project: dict) -> list[dict[str, str]]:
    return [file for file in project["files"] if file["path"].endswith(".py")]


def ensure_lesson_sections(project: dict, folder_name: str, body: str) -> str:
    link = f"{REPO_URL}/tree/main/projects/{folder_name}"
    if link not in body:
        body = f"Repository:\n{link}\n\n" + body
    if "Full Code:" not in body:
        body += "\n\nFull Code:\n"
        for file in main_code_files(project):
            body += f"\n{file['path']}\n```python\n{file['content']}\n```\n"
    if "Line-by-Line Code Explanation:" not in body:
        body += "\n\n"
        for file in main_code_files(project):
            body += f"{file['path']}\n{line_explanation(file['content'])}\n"
    return sanitize_text(body)


def send_lesson(subject: str, body: str) -> None:
    to_email = os.getenv("LESSON_EMAIL_TO")
    host = os.getenv("SMTP_HOST")
    username = os.getenv("SMTP_USERNAME")
    password = os.getenv("SMTP_PASSWORD")
    port = int(os.getenv("SMTP_PORT", "587"))
    if not all([to_email, host, username, password]):
        print("Email settings are incomplete; skipping lesson email.")
        return

    message = EmailMessage()
    message["Subject"] = subject
    message["From"] = username
    message["To"] = to_email
    message.set_content(body)
    with smtplib.SMTP(host, port) as server:
        server.starttls()
        server.login(username, password)
        server.send_message(message)


def main() -> None:
    date_text = today_ist().strftime("%Y-%m-%d")
    project = choose_project(date_text)
    folder_name = unique_folder_name(date_text, project["slug"])
    folder = PROJECTS_DIR / folder_name

    for file in project["files"]:
        write_text(folder / file["path"], file["content"])
    update_root_readme(project, folder_name, date_text)

    email_body = project.get("lesson_email") or f"Project Generated: {project['title']}\n"
    email_body = ensure_lesson_sections(project, folder_name, email_body.replace("<folder>", folder_name))
    send_lesson(f"Daily Biotech Project Lesson - {project['title']}", email_body)
    print(f"Created project: {folder_name}")


if __name__ == "__main__":
    main()
