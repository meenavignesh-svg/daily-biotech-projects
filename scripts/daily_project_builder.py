def line_explanation(code: str) -> str:
    parts = ["Line-by-Line Code Explanation:", ""]
    for number, text in enumerate(code.splitlines(), start=1):
        stripped = text.strip()
        if not stripped:
            meaning = "This blank line separates sections so the script is easier to read."
        elif stripped.startswith('"""'):
            meaning = "This describes the purpose of the script."
        elif stripped.startswith("from __future__"):
            meaning = "This keeps modern Python type-hint behavior consistent."
        elif stripped.startswith("import ") or stripped.startswith("from "):
            meaning = "This imports a module the script needs."
        elif stripped.startswith("def "):
            meaning = "This starts a reusable function."
        elif stripped.startswith("return "):
            meaning = "This sends a result back from the function."
        elif stripped.startswith("if __name__"):
            meaning = "This runs main() only when the file is opened directly."
        elif "argparse" in stripped or "add_argument" in stripped:
            meaning = "This helps the script accept input from the command line."
        elif "print(" in stripped:
            meaning = "This displays the result for the user."
        else:
            meaning = "This line supports the main project logic."
        parts.append(f"Line {number}: `{text}`")
        parts.append(f"Explanation: {meaning}")
        parts.append("")
    return "\n".join(parts)


def main_code_files(project: dict) -> list[dict[str, str]]:
    return [file for file in project["files"] if file["path"].endswith(".py")]


def ensure_lesson_sections(project: dict, folder_name: str, body: str) -> str:
    link = f"{REPO_URL}/tree/main/projects/{folder_name}"
    if link not in body:
        body = f"Repository:\n{link}\n\n" + body
    if "Full Code:" not in body:
        body += "\n\nFull Code:\n"
        for file in main_code_files(project):
            body += f"\n{file['path']}\n```python\n{file['content']}\n```\n"
    if "Line-by-Line Code Explanation:" not in body:
        body += "\n\n"
        for file in main_code_files(project):
            body += f"{file['path']}\n{line_explanation(file['content'])}\n"
    return sanitize_text(body)


def send_lesson(subject: str, body: str) -> None:
    to_email = os.getenv("LESSON_EMAIL_TO")
    host = os.getenv("SMTP_HOST")
    username = os.getenv("SMTP_USERNAME")
    password = os.getenv("SMTP_PASSWORD")
    port = int(os.getenv("SMTP_PORT", "587"))
    if not all([to_email, host, username, password]):
        print("Email settings are incomplete; skipping lesson email.")
        return
    message = EmailMessage()
    message["Subject"] = subject
    message["From"] = username
    message["To"] = to_email
    message.set_content(body)
    with smtplib.SMTP(host, port) as server:
        server.starttls()
        server.login(username, password)
        server.send_message(message)


def main() -> None:
    date_text = today_ist().strftime("%Y-%m-%d")
    track = target_track()
