RULE_WEIGHTS = {
    "unknown_source": 2,
    "temperature_break": 2,
    "missing_label": 1,
    "contamination_note": 3,
}


def score_row(row: dict[str, str]) -> tuple[int, list[str]]:
    score = 0
    reasons: list[str] = []
    for rule, weight in RULE_WEIGHTS.items():
        if row.get(rule, "no").strip().lower() == "yes":
            score += weight
            reasons.append(rule.replace("_", " "))
    return score, reasons


def category(score: int) -> str:
    if score >= 4:
        return "high review priority"
    if score >= 2:
        return "medium review priority"
    return "low review priority"


