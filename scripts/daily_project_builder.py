def melting_temperature(sequence: str) -> int:
    return 2 * (sequence.count("A") + sequence.count("T")) + 4 * (sequence.count("G") + sequence.count("C"))


def primer_status(sequence: str) -> str:
    length_ok = 18 <= len(sequence) <= 25
    gc_ok = 40 <= gc_content(sequence) <= 60
    tm_ok = 50 <= melting_temperature(sequence) <= 65
    return "PASS" if length_ok and gc_ok and tm_ok else "REVIEW"


