def send_lesson(subject: str, body: str) -> None:
    to_email = os.getenv("LESSON_EMAIL_TO")
    host = os.getenv("SMTP_HOST")
    username = os.getenv("SMTP_USERNAME")
    password = os.getenv("SMTP_PASSWORD")
    port = int(os.getenv("SMTP_PORT", "587"))
    if not all([to_email, host, username, password]):
        print("Email settings are incomplete; skipping lesson email.")
        return
    try:
        message = EmailMessage()
        message["Subject"] = subject
        message["From"] = username
        message["To"] = to_email
        message.set_content(body)
        with smtplib.SMTP(host, port, timeout=30) as server:
            server.starttls()
            server.login(username, password)
            server.send_message(message)
    except Exception as exc:
        print(f"Lesson email failed, but project files were created: {exc}")


def append_resume_bullet(item: dict[str, str]) -> None:
    header = "# Resume Bullets\n\nThese are portfolio-ready bullets I can adapt for internship and entry-level applications.\n\n"
    bullet = f"- {item['resume']}"
    if RESUME_PATH.exists():
        text = RESUME_PATH.read_text(encoding="utf-8")
    else:
        text = header
    if bullet not in text:
        if not text.endswith("\n"):
            text += "\n"
        text += bullet + "\n"
    RESUME_PATH.write_text(clean_public_text(text) + "\n", encoding="utf-8")


def write_project(item: dict[str, str]) -> None:
    files = make_files(item["kind"])
    folder = project_path(item)
    folder.mkdir(parents=True, exist_ok=True)
    (folder / "README.md").write_text(readme_for(item, files), encoding="utf-8")
    (folder / files.sample_name).write_text(files.sample_content, encoding="utf-8")
    (folder / files.script_name).write_text(clean_public_text(files.code), encoding="utf-8")
    (folder / "example_output.txt").write_text(files.example_output, encoding="utf-8")
    append_resume_bullet(item)
    send_lesson(f"Biotech job skill lesson - {item['title']}", email_body(item, files))
    print(f"Created {TRACKS[item['folder']]} project: {item['title']}")


