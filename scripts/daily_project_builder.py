def today_ist() -> datetime:
    return datetime.now(IST)


def slugify(text: str) -> str:
    clean = re.sub(r"[^a-z0-9]+", "-", text.lower())
    return clean.strip("-") or "daily-project"


def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def existing_project_names() -> str:
    if not PROJECTS_DIR.exists():
        return "No existing projects found."
    names = sorted(path.name for path in PROJECTS_DIR.iterdir() if path.is_dir())
    return "\n".join(names[-30:]) if names else "No existing projects found."


def openai_text(prompt: str, max_tokens: int = 1600) -> str:
    api_key = os.getenv("OPENAI_API_KEY")
    if not api_key:
        raise RuntimeError("OPENAI_API_KEY is not set")

    model = os.getenv("OPENAI_MODEL") or "gpt-4.1-mini"
    payload = {
        "model": model,
        "input": prompt,
        "max_output_tokens": max_tokens,
    }
    request = urllib.request.Request(
        "https://api.openai.com/v1/responses",
        data=json.dumps(payload).encode("utf-8"),
        headers={
            "Authorization": f"Bearer {api_key}",
            "Content-Type": "application/json",
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(request, timeout=90) as response:
            data = json.loads(response.read().decode("utf-8"))
    except urllib.error.HTTPError as exc:
        detail = exc.read().decode("utf-8", errors="replace")
        raise RuntimeError(f"OpenAI API request failed: {detail}") from exc

    texts: list[str] = []
    for item in data.get("output", []):
        for content in item.get("content", []):
            text = content.get("text")
            if text:
                texts.append(text)
    if not texts and data.get("output_text"):
        texts.append(data["output_text"])
    if not texts:
        raise RuntimeError("No text returned from model")
    return "\n".join(texts).strip()


def extract_json(text: str) -> dict:
    fenced = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", text, flags=re.DOTALL)
    if fenced:
        text = fenced.group(1)
    else:
        start = text.find("{")
        end = text.rfind("}")
        if start != -1 and end != -1:
            text = text[start : end + 1]
    return json.loads(text)


