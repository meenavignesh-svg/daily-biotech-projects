def unique_folder_name(date_text: str, slug: str) -> str:
    base = f"{date_text}-{slug}"
    candidate = base
    counter = 2
    while (PROJECTS_DIR / candidate).exists():
        candidate = f"{base}-{counter}"
        counter += 1
    return candidate


def project_meta(folder: Path) -> dict[str, str]:
    readme_path = folder / "README.md"
    text = readme_path.read_text(encoding="utf-8") if readme_path.exists() else ""
    title_match = re.search(r"^#\s+(.+)$", text, flags=re.MULTILINE)
    track_match = re.search(r"\*\*Track:\*\*\s*(.+?)\s*(?:\n|$)", text)
    concept_match = re.search(r"## (?:Concept|Biotech Concept)\s+(.+?)(?:\n## |\Z)", text, flags=re.DOTALL)
    title = title_match.group(1).strip() if title_match else folder.name
    track = track_match.group(1).strip() if track_match else "Bioinformatics"
    if track not in TRACKS:
        track = "Bioinformatics"
    concept = " ".join(concept_match.group(1).strip().split()) if concept_match else "Student biotechnology portfolio project"
    date = folder.name[:10] if re.match(r"\d{4}-\d{2}-\d{2}", folder.name) else "Unknown"
    return {"date": date, "title": title, "track": track, "concept": concept, "folder": folder.name}


