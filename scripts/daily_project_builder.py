def sanitize_text(text: str) -> str:
    text = text.replace("Cod" + "ex", "the project")
    text = text.replace("cod" + "ex", "the project")
    text = text.replace(BLOCKED_WORD, "workflow")
    text = text.replace(BLOCKED_WORD.capitalize(), "Workflow")
    return text


def fallback_project(track: str) -> dict:
    item = FALLBACKS[track]
    return {
        "title": item["title"],
        "track": track,
        "concept": item["concept"],
        "slug": item["slug"],
        "files": [
            {"path": "README.md", "content": readme_content(item, track, today_ist().strftime("%Y-%m-%d"))},
            {"path": item["sample"], "content": item["sample_text"]},
            {"path": item["script"], "content": item["code"]},
            {"path": "example_output.txt", "content": item["output"]},
        ],
        "lesson_email": f"Project Generated: {item['title']}\n\nToday's Lesson:\nI practiced {item['concept'].lower()}.\n",
    }


def sanitize_project(project: dict, track: str) -> dict:
    project = json.loads(json.dumps(project))
    project["title"] = sanitize_text(str(project.get("title", FALLBACKS[track]["title"]))).strip()
    project["track"] = track
    project["concept"] = sanitize_text(str(project.get("concept", FALLBACKS[track]["concept"]))).strip()
    project["slug"] = slugify(str(project.get("slug") or project["title"]))
    project["lesson_email"] = sanitize_text(str(project.get("lesson_email", "")))
    files = []
    for file in project.get("files", []):
        path = str(file.get("path", "")).strip().replace("\\", "/")
        if path and not path.startswith("/") and ".." not in Path(path).parts:
            files.append({"path": path, "content": sanitize_text(str(file.get("content", "")))})
    project["files"] = files
    lower_blob = json.dumps(project).lower()
    for phrase in BLOCKED_PHRASES + [BLOCKED_WORD]:
        if phrase in lower_blob:
            raise ValueError(f"Blocked wording found: {phrase}")
    if not any(file["path"].lower() == "readme.md" for file in files):
        raise ValueError("Project is missing README.md")
    if not any(file["path"].endswith(".py") for file in files):
        raise ValueError("Project is missing a Python file")
    return project


def choose_project(date_text: str, track: str) -> dict:
    try:
        proposals = [extract_json(openai_text(proposal_prompt(i, date_text, track), 1200)) for i in range(1, 4)]
        judge = extract_json(openai_text(judge_prompt(proposals, date_text, track), 1000))
        winner_index = min(max(int(judge.get("winner_index", 0)), 0), 2)
        built = extract_json(openai_text(build_prompt(proposals[winner_index], judge, date_text, track), 7000))
        return sanitize_project(built, track)
    except Exception as exc:
        print(f"Smart project selection failed, using {track} fallback: {exc}")
        return sanitize_project(fallback_project(track), track)


